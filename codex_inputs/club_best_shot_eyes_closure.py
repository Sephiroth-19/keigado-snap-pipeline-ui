def chunk_list(items: List[str], size: int) -> List[List[str]]:
    if size <= 0:
        raise ValueError("FIXED_GROUP_SIZE must be > 0")
    return [items[i : i + size] for i in range(0, len(items), size)]


def detect_adjacent_same_group(prev_path: str, curr_path: str):
    try:
        b64_prev = encode_image_path_to_base64(prev_path)
        b64_curr = encode_image_path_to_base64(curr_path)
        raw = call_gpt5_two_images_json(
            client,
            b64_prev,
            b64_curr,
            GROUP_BOUNDARY_PROMPT,
            MODEL_NAME,
        )
        data = parse_json_object(raw)

        same_group = bool(data.get("same_group", True))
        confidence = str(data.get("confidence", "low")).lower()
        reason = data.get("reason", "")
        adjusted = False

        # Avoid over-splitting when the boundary confidence is weak.
        if (not same_group) and confidence == "low" and TREAT_LOW_CONF_BOUNDARY_AS_SAME:
            same_group = True
            adjusted = True
            reason = "Low-confidence boundary was treated as same group."

        return same_group, confidence, reason, adjusted

    except Exception as e:
        # Fail-safe: keep same group to avoid accidental split from transient API issues.
        return True, "low", f"Boundary check failed: {e}", True


def build_groups_auto_adjacent(image_paths: List[str]):
    if not image_paths:
        return [], []

    if len(image_paths) == 1:
        return [image_paths], []

    groups = []
    decisions = []
    current_group = [image_paths[0]]

    for i in tqdm(range(1, len(image_paths)), desc="Auto grouping"):
        prev_path = image_paths[i - 1]
        curr_path = image_paths[i]

        same_group, confidence, reason, adjusted = detect_adjacent_same_group(prev_path, curr_path)

        decisions.append(
            {
                "pair_index": i,
                "prev_file": os.path.basename(prev_path),
                "curr_file": os.path.basename(curr_path),
                "same_group": same_group,
                "confidence": confidence,
                "adjusted": adjusted,
                "reason": reason,
            }
        )

        if same_group:
            current_group.append(curr_path)
        else:
            groups.append(current_group)
            current_group = [curr_path]

    groups.append(current_group)
    return groups, decisions


def build_groups(image_paths: List[str]) -> List[List[str]]:
    if not image_paths:
        return []

    if GROUPING_MODE == "all_in_one":
        return [image_paths]

    if GROUPING_MODE == "auto_adjacent_gpt":
        groups_auto, decisions = build_groups_auto_adjacent(image_paths)
        globals()["auto_group_decisions"] = decisions
        return groups_auto

    if GROUPING_MODE == "fixed_size":
        return chunk_list(image_paths, FIXED_GROUP_SIZE)

    if GROUPING_MODE == "sizes":
        groups = []
        cursor = 0

        for n in GROUP_SIZES:
            if n <= 0:
                continue
            if cursor >= len(image_paths):
                break
            groups.append(image_paths[cursor : cursor + n])
            cursor += n

        if cursor < len(image_paths):
            groups.append(image_paths[cursor:])
            print(
                "Warning: GROUP_SIZES did not cover all images. "
                f"Added trailing group with {len(image_paths[cursor:])} image(s)."
            )

        return [g for g in groups if g]

    raise ValueError("GROUPING_MODE must be one of: sizes, fixed_size, all_in_one, auto_adjacent_gpt")


