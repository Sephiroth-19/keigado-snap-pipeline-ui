def safe_concat(dfs: List[pd.DataFrame], default_cols=None) -> pd.DataFrame:
    valid = []
    for df in dfs:
        if isinstance(df, pd.DataFrame) and len(df.columns) > 0 and len(df) > 0:
            valid.append(df)
    if valid:
        return pd.concat(valid, ignore_index=True)
    return pd.DataFrame(columns=default_cols or [])


def unique_path(path: str) -> str:
    if not os.path.exists(path):
        return path
    base, ext = os.path.splitext(path)
    k = 2
    while True:
        candidate = f"{base}__dup{k}{ext}"
        if not os.path.exists(candidate):
            return candidate
        k += 1


def write_df_to_worksheet(ws, df: pd.DataFrame):
    if df is None or not isinstance(df, pd.DataFrame):
        df = pd.DataFrame()

    df_to_write = df.copy()
    for col in df_to_write.columns:
        df_to_write[col] = df_to_write[col].apply(
            lambda x: "" if pd.isna(x) else str(x) if isinstance(x, (list, dict, tuple, set)) else x
        )

    for row in dataframe_to_rows(df_to_write, index=False, header=True):
        ws.append(row)

    for col_cells in ws.columns:
        col_letter = col_cells[0].column_letter
        max_len = 0
        for cell in col_cells:
            try:
                cell_len = len(str(cell.value)) if cell.value is not None else 0
                if cell_len > max_len:
                    max_len = cell_len
            except Exception:
                pass
        ws.column_dimensions[col_letter].width = min(max_len + 2, 40)


