def get_file_date_string(path: str) -> str:
    # 1) Try EXIF DateTimeOriginal
    try:
        img = Image.open(path)
        exif = img._getexif()
        if exif:
            for tag_id, value in exif.items():
                tag = ExifTags.TAGS.get(tag_id, tag_id)
                if tag == "DateTimeOriginal" and value:
                    # Format: "YYYY:MM:DD HH:MM:SS" -> "YYYYMMDD"
                    return str(value)[:10].replace(":", "")
    except Exception:
        pass

    # 2) Fallback to file modified date if EXIF is missing
    try:
        mod_time = os.path.getmtime(path)
        return datetime.datetime.fromtimestamp(mod_time).strftime("%Y%m%d")
    except Exception:
        return "00000000"

