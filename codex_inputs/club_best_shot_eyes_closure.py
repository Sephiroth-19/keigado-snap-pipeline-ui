def upload_images_to_folder(dst_dir: str) -> List[str]:
    ensure_dir(dst_dir)
    uploaded = files.upload()

    # Move/extract into dst_dir
    for fn in uploaded.keys():
        src = f"/content/{fn}"

        if fn.lower().endswith(".zip"):
            with zipfile.ZipFile(src, "r") as z:
                z.extractall(dst_dir)
            os.remove(src)
        else:
            shutil.move(src, os.path.join(dst_dir, fn))

    # Collect image paths (recursive, because zip may contain subfolders)
    exts = (".png", ".jpg", ".jpeg", ".webp", ".bmp", ".tif", ".tiff")
    image_paths = []
    for root, _, files_ in os.walk(dst_dir):
        for f in files_:
            if f.lower().endswith(exts):
                image_paths.append(os.path.join(root, f))

    image_paths = sorted(image_paths)
    print(f"Uploaded images found: {len(image_paths)}")
    if len(image_paths) > 0:
        print("Example:", image_paths[0])
    return image_paths

