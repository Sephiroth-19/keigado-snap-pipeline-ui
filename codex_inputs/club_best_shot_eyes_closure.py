ts         = datetime.datetime.now().strftime("%Y%m%d_%H%M%S")
MARKED_DIR = os.path.join(LOCAL_OUTPUT_DIR, f"{ts}_marked")
CLEAN_DIR  = os.path.join(LOCAL_OUTPUT_DIR, f"{ts}_clean")
