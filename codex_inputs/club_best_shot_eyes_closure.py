def _write_sheet(wb, title, df):
    ws = wb.create_sheet(title=title)
    if df is None or df.empty:
        ws.append(["No data"]); return
    df2 = df.copy()
    for col in df2.columns:
        df2[col] = df2[col].apply(
            lambda x: "" if pd.isna(x) else str(x) if isinstance(x, (list,dict,set)) else x)
    for row in dataframe_to_rows(df2, index=False, header=True):
        ws.append(row)
    for cc in ws.columns:
        ws.column_dimensions[cc[0].column_letter].width = min(
            max(len(str(c.value or "")) for c in cc) + 2, 45)


