def calculate_rankings(df_eval, group_idx, allow_formality_issues=False):
    df_temp = df_eval.copy()

    # Strict rejection logic updated to include new client-requested flags
    if STRICT_FORMALITY_REJECTION and not allow_formality_issues:
        gesture_mask = df_temp["has_hand_gesture"] == True
        laugh_mask = df_temp["has_exaggerated_expression"] == True
        eyes_mask = df_temp["eyes_closed"] == True
        face_mask = df_temp["face_obscured"] == True
        df_temp.loc[gesture_mask | laugh_mask | eyes_mask | face_mask, "is_ng"] = True

    df_ok = df_temp[df_temp["is_ng"] == False].copy()

    if df_ok.empty:
        return df_ok

    for col in ["formality_score", "beauty_score", "expression_score", "emotion_score", "gesture_expression_penalty"]:
        df_ok[col] = pd.to_numeric(df_ok[col], errors="coerce").fillna(0)

    df_ok["rule_penalty"] = (
        df_ok["gesture_expression_penalty"] * 0.7
        + df_ok["has_hand_gesture"].astype(int) * 2.0
        + df_ok["has_exaggerated_expression"].astype(int) * 1.5
    )

    # Fixed the indexing error here
    df_ok["total_score"] = (
        df_ok["formality_score"] * 0.35
        + df_ok["beauty_score"] * 0.30
        + df_ok["expression_score"] * 0.20
        + df_ok["emotion_score"] * 0.15
        - df_ok["rule_penalty"]
    )

    df_ok = df_ok.sort_values("total_score", ascending=False).reset_index(drop=True)
    df_ok["rank"] = range(1, len(df_ok) + 1)
    return df_ok

