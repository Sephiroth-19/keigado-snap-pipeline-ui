GPT_PROMPT = """
You are examining a CROPPED IMAGE of a single FACE. Your task is to determine if the eyes are closed.

STEP 1 — has_clear_face: Can you see this person's eye region?
  TRUE  → Eye area is visible (even if eyes are closed, angled, or looking down).
  FALSE → Only back of head / extreme side profile / eyes fully blocked by hair, hands,
          or mask / image too blurry or small to judge.

STEP 2 — eyes_closed: Are the eyes closed?
  TRUE  → The upper eyelid is resting DOWN on or past the lower eyelid. No iris or pupil
          is visible at all. The person is sleeping, blinking, or has eyes shut.
  FALSE → ANY part of the dark iris or pupil is visible, even a small sliver or crescent.

THE MOST COMMON MISTAKE — smiling squint:
  When a person smiles broadly, their cheeks rise and compress the eye opening, making
  eyes look very narrow or almost shut. This is NOT closed eyes.
  Key test: look for the dark iris/pupil. If you can see ANY dark circle or crescent
  behind the eyelid gap → eyes are OPEN (FALSE), regardless of how narrow the gap is.
  The eye shape being narrow or curved/crescent from smiling ≠ closed.

RULES for East Asian faces:
  - Naturally narrow or monolid eyes show only a thin iris line when open → FALSE.
  - Smiling/laughing with crescent-shaped eye openings → still FALSE if iris is visible.
  - Only return TRUE if the eyelid is fully covering the eye with NO iris visible at all.

OTHER CASES:
  - Looking down but pupils visible through eyelid gap → FALSE (open).
  - Glasses do not affect this judgment — look at the eye behind the lens.
  - If has_clear_face is FALSE, set eyes_closed to FALSE.

Return ONLY this JSON (no markdown, no extra text):
{"has_clear_face": true|false, "eyes_closed": true|false, "confidence": "high"|"medium"|"low"}
""".strip()


def pad_bbox(x1, y1, x2, y2, img_w, img_h, pad_pct=FACE_PADDING_PCT):
    """Add percentage-based padding around a face bbox, clamped to image bounds."""
    w, h   = x2 - x1, y2 - y1
    pad_x  = int(w * pad_pct)
    pad_y  = int(h * pad_pct)
    return (
        max(0,     x1 - pad_x),
        max(0,     y1 - pad_y),
        min(img_w, x2 + pad_x),
        min(img_h, y2 + pad_y),
    )


def encode_crop(bgr_crop):
    _, buf = cv2.imencode(".jpg", bgr_crop, [cv2.IMWRITE_JPEG_QUALITY, 93])
    return base64.b64encode(buf).decode("utf-8")


def ask_gpt(face_crop_bgr):
    """
    Send face crop to GPT-5.4.
    Returns (has_clear_face, eyes_closed, confidence).
    """
    h, w = face_crop_bgr.shape[:2]
    if w < GPT_MIN_FACE_PX or h < GPT_MIN_FACE_PX:
        return False, False, "low"

    b64 = encode_crop(face_crop_bgr)

    for attempt in range(GPT_RETRY):
        try:
            resp = client.responses.create(
                model=GPT_MODEL,
                input=[{
                    "role": "user",
                    "content": [
                        {"type": "input_text",  "text": GPT_PROMPT},
                        {"type": "input_image",
                         "image_url": f"data:image/jpeg;base64,{b64}",
                         "detail": "high"},
                    ],
                }],
                reasoning={"effort": "medium"},
            )
            text = resp.output_text.strip()
            # Strip markdown fences if present
            if "```" in text:
                text = text.split("```")[1]
                if text.startswith("json"): text = text[4:]
            data = json.loads(text.strip())
            return (
                bool(data.get("has_clear_face", False)),
                bool(data.get("eyes_closed",    False)),
                str(data.get("confidence",      "medium")),
            )
        except Exception as e:
            if attempt == GPT_RETRY - 1:
                print(f"    GPT error: {e}")
    return False, False, "low"


def draw_corner_box(img, x1, y1, x2, y2, color, thickness):
    """Draw rectangle with corner accent marks."""
    cv2.rectangle(img, (x1, y1), (x2, y2), color, thickness)
    cl = max(8, (x2 - x1) // 8)
    for cx, cy, dx, dy in [(x1,y1,1,1),(x2,y1,-1,1),(x1,y2,1,-1),(x2,y2,-1,-1)]:
        cv2.line(img, (cx,cy), (cx+dx*cl, cy),      color, thickness+1)
        cv2.line(img, (cx,cy), (cx, cy+dy*cl),      color, thickness+1)


def draw_label(img, text, x1, y1, color):
    font, scale, thick = cv2.FONT_HERSHEY_DUPLEX, 0.55, 1
    (tw, th), _ = cv2.getTextSize(text, font, scale, thick)
    pad = 4
    label_y = y1 - th - pad*2 if y1 - th - pad*2 > 0 else y1 + th + pad*2 + 2
    cv2.rectangle(img, (x1, label_y-th-pad), (x1+tw+pad*2, label_y+pad), color, -1)
    cv2.putText(img, text, (x1+pad, label_y), font, scale, (255,255,255), thick, cv2.LINE_AA)


