all_results   = []
all_face_rows = []

for img_path in tqdm(image_paths, desc="Processing"):
    fname     = os.path.basename(img_path)
    stem, _   = os.path.splitext(fname)

    bgr = cv2.imread(img_path)
    if bgr is None:
        print(f"  SKIP (load failed): {fname}")
        all_results.append({"file_name": fname, "status": "load_error"})
        continue

    img_h, img_w = bgr.shape[:2]
    annotated    = bgr.copy()
    thickness    = max(2, img_h // 400)

    # ── Step 1: InsightFace face detection ───────────────────
    # InsightFace expects RGB
    rgb   = cv2.cvtColor(bgr, cv2.COLOR_BGR2RGB)
    faces = face_app.get(rgb)   # list of Face objects

    clear_faces      = 0
    closed_eye_faces = 0

    for f_idx, face in enumerate(faces):
        # face.bbox → [x1, y1, x2, y2] float
        x1, y1, x2, y2 = map(int, face.bbox)
        x1, y1 = max(0, x1), max(0, y1)
        x2, y2 = min(img_w, x2), min(img_h, y2)

        # Padded face crop for GPT
        px1, py1, px2, py2 = pad_bbox(x1, y1, x2, y2, img_w, img_h)
        face_crop = bgr[py1:py2, px1:px2]

        if face_crop.size == 0:
            continue

        # ── Step 2: GPT judgment ─────────────────────────────
        has_clear_face, eyes_closed, confidence = ask_gpt(face_crop)

        all_face_rows.append({
            "file_name"     : fname,
            "face_index"    : f_idx + 1,
            "det_score"     : round(float(face.det_score), 3),
            "has_clear_face": has_clear_face,
            "eyes_closed"   : eyes_closed,
            "confidence"    : confidence,
            "bbox"          : f"{x1},{y1},{x2},{y2}",
        })

        # ── Step 3: Draw annotation ───────────────────────────
        if has_clear_face:
            clear_faces += 1
            if eyes_closed:
                closed_eye_faces += 1
                draw_corner_box(annotated, x1, y1, x2, y2, COLOR_CLOSED, thickness)
                draw_label(annotated, f"EYES CLOSED [{confidence[0].upper()}]", x1, y1, COLOR_CLOSED)
            else:
                draw_corner_box(annotated, x1, y1, x2, y2, COLOR_OPEN, thickness)
                if SHOW_OPEN_LABEL:
                    draw_label(annotated, "OPEN", x1, y1, COLOR_OPEN)
        else:
            # Thin yellow box — face detected but occluded/unclear
            draw_corner_box(annotated, x1, y1, x2, y2, COLOR_NO_FACE, max(1, thickness//2))

    # ── Step 4: Counter overlay ───────────────────────────────
    font_scale = max(0.6, img_h / 700)
    ot  = max(2, thickness)
    oy  = int(0.06 * img_h)
    gap = int(oy * 0.75)

    overlay_lines = [
        (f"People Detected : {len(faces)}",      (200, 200, 200)),
        (f"Clear Faces     : {clear_faces}",      COLOR_OPEN),
        (f"Eyes Closed     : {closed_eye_faces}", COLOR_CLOSED),
    ]
    for i, (text, color) in enumerate(overlay_lines):
        cv2.putText(annotated, text,
                    (int(0.02*img_w), oy + i * gap),
                    cv2.FONT_HERSHEY_SIMPLEX, font_scale, color, ot)

    # ── Step 5: Save ──────────────────────────────────────────
    cv2.imwrite(os.path.join(MARKED_DIR, f"{stem}_marked.jpg"), annotated, [cv2.IMWRITE_JPEG_QUALITY, 95])
    cv2.imwrite(os.path.join(CLEAN_DIR,  f"{stem}_clean.jpg"),  bgr,       [cv2.IMWRITE_JPEG_QUALITY, 95])

    all_results.append({
        "file_name"       : fname,
        "status"          : "ok",
        "faces_detected"  : len(faces),
        "clear_faces"     : clear_faces,
        "closed_eye_faces": closed_eye_faces,
        "has_closed_eyes" : closed_eye_faces > 0,
    })

    closed_flag = "⚠" if closed_eye_faces > 0 else "✓"
    print(f"  {closed_flag}  {fname}  →  people: {len(faces)}  |  clear faces: {clear_faces}  |  eyes closed: {closed_eye_faces}")

print(f"\n✓ Done. Marked → {MARKED_DIR}")


