def format_roster_rows_for_prompt(page_rows, max_rows_per_chunk=18):
    chunks, current = [], []
    for row in page_rows:
        line = (
            f'ROW {row["row_num"]:03d} || '
            f'subject_raw="{row.get("subject_raw","")}" || '
            f'role_raw="{row.get("role_raw","")}" || '
            f'grade_raw="{row.get("grade_raw","")}" || '
            f'surname_raw="{row.get("surname_raw","")}" || '
            f'given_name_raw="{row.get("given_name_raw","")}" || '
            f'row_text="{row.get("row_text","")}"'
        )
        current.append(line)
        if len(current) >= max_rows_per_chunk:
            chunks.append("\n".join(current))
            current = []
    if current:
        chunks.append("\n".join(current))
    return chunks

def call_gpt_roster_structured_rows_page(filename: str, page_num: int, page_rows: list) -> list:
    if not page_rows:
        return []
    row_chunks = format_roster_rows_for_prompt(page_rows, max_rows_per_chunk=18)
    records_all = []
    for chunk_idx, row_chunk in enumerate(row_chunks, start=1):
        instruction = ROSTER_ROW_REPAIR_INSTRUCTION_TEMPLATE.format(
            filename=filename, page_num=page_num, json_schema=PER_FILE_JSON_SCHEMA
        )
        prompt = instruction + f"\n\nROW CHUNK {chunk_idx}/{len(row_chunks)}:\n" + row_chunk
        resp = client.chat.completions.create(
            model=MODEL_TEXT,
            messages=[
                {"role": "system", "content": "You repair and normalize structured teacher roster rows carefully."},
                {"role": "user",   "content": prompt},
            ],
        )
        raw = resp.choices[0].message.content
        obj = safe_json_object_load(raw, default={"file": filename, "page_num": page_num, "records": []})
        for r in obj.get("records", []) or []:
            if isinstance(r, dict):
                records_all.append(r)
    return records_all

