def _json_safe(value):
    """Convert common numpy/path objects into JSON-serialisable values."""
    try:
        import numpy as _np
        if isinstance(value, (_np.integer,)):
            return int(value)
        if isinstance(value, (_np.floating,)):
            return float(value)
        if isinstance(value, (_np.ndarray,)):
            return value.tolist()
    except Exception:
        pass
    if isinstance(value, Path):
        return str(value)
    if isinstance(value, dict):
        return {str(k): _json_safe(v) for k, v in value.items() if not str(k).startswith("_")}
    if isinstance(value, list):
        return [_json_safe(v) for v in value]
    if isinstance(value, tuple):
        return [_json_safe(v) for v in value]
    return value


def _public_row(row: dict) -> dict:
    """Keep internal helper fields out of API response."""
    return {k: _json_safe(v) for k, v in row.items() if not k.startswith("_")}


def _collect_flat_images(images_dir: str | Path) -> list[str]:
    """
    Same structure as the current Menna code: read only first-level files from one flat folder.
    No per-teacher subfolder logic is added here.
    """
    root = Path(images_dir)
    return sorted([
        str(p) for p in root.iterdir()
        if p.is_file() and p.suffix.lower() in {ext.lower() for ext in IMAGE_EXTENSIONS}
    ])


def _make_zip(src_dir: Path, zip_path: Path) -> str:
    import zipfile
    zip_path.parent.mkdir(parents=True, exist_ok=True)
    with zipfile.ZipFile(zip_path, "w", zipfile.ZIP_DEFLATED) as zf:
        for p in src_dir.rglob("*"):
            if p.is_file():
                zf.write(p, p.relative_to(src_dir))
    return str(zip_path)


def run_teacher_pipeline(
    images_dir: str | Path,
    roster_pdf_path: str | Path,
    output_dir: str | Path,
    *,
    api_key: str | None = None,
    model_text: str | None = None,
    model_vision: str | None = None,
    face_match_threshold: float = 0.40,
) -> dict:
    """
    Web-backend callable Teacher Photos pipeline.

    Inputs:
      - images_dir: flat folder containing teacher card photos and portrait photos.
      - roster_pdf_path: teacher roster PDF.
      - output_dir: pipeline output directory.

    Outputs:
      - Processed/: matched card shots, renamed with 札xx
      - BestShot/: matched portrait shots, ranked/renamed with 本xx
      - Unknown/: unmatched card shots and unmatched portraits
      - results_v12.xlsx: Excel report with match, staff master, best-shot, subject sheets
      - output.zip: zipped output directory
      - JSON dict for Web UI tabs
    """
    global IMAGES_FOLDER, PDF_PATH, _BASE_OUT
    global PROCESSED_FOLDER, BEST_SHOT_FOLDER, UNKNOWN_FOLDER, EXCEL_OUTPUT
    global FACE_MATCH_THRESHOLD, _pdf_staff_master

    configure_openai_client(api_key=api_key, model_text=model_text, model_vision=model_vision)

    images_dir = Path(images_dir).resolve()
    roster_pdf_path = Path(roster_pdf_path).resolve()
    output_dir = Path(output_dir).resolve()

    if not images_dir.exists() or not images_dir.is_dir():
        raise FileNotFoundError(f"images_dir not found or not a directory: {images_dir}")
    if not roster_pdf_path.exists() or roster_pdf_path.suffix.lower() != ".pdf":
        raise FileNotFoundError(f"roster_pdf_path must be an existing PDF file: {roster_pdf_path}")

    IMAGES_FOLDER = str(images_dir)
    PDF_PATH = str(roster_pdf_path)
    _BASE_OUT = str(output_dir)
    PROCESSED_FOLDER = str(output_dir / "Processed")
    BEST_SHOT_FOLDER = str(output_dir / "BestShot")
    UNKNOWN_FOLDER = str(output_dir / "Unknown")
    EXCEL_OUTPUT = str(output_dir / "results_v12.xlsx")
    FACE_MATCH_THRESHOLD = float(face_match_threshold)

    output_dir.mkdir(parents=True, exist_ok=True)
    Path(PROCESSED_FOLDER).mkdir(parents=True, exist_ok=True)
    Path(BEST_SHOT_FOLDER).mkdir(parents=True, exist_ok=True)
    Path(UNKNOWN_FOLDER).mkdir(parents=True, exist_ok=True)

    image_paths = _collect_flat_images(images_dir)
    if not image_paths:
        raise FileNotFoundError(f"No images found in flat folder: {images_dir}")

    # Reset per-run state.
    _card_shot_counter.clear()
    _pdf_staff_master = []

    # Step 1: Extract staff list from PDF.
    staff_list = extract_staff_from_pdf(str(roster_pdf_path))

    # Steps 2 + 3: classify image, read card, match card with PDF staff list.
    raw_results = []
    results = []

    for image_path in image_paths:
        info = extract_image_info(image_path)
        info["image_path"] = str(image_path)
        info["filename"] = Path(image_path).name

        if "error" in info:
            raw_results.append(info)
            results.append({
                "filename": info["filename"],
                "renamed_filename": None,
                "image_path": str(image_path),
                "card_name": None,
                "card_subject": None,
                "card_ocr_confidence": None,
                "card_uncertain_chars": "",
                "status": f"GPT error: {info['error']}",
                "match_method": "none",
                "match_score": 0.0,
                "pdf_name": None,
                "pdf_subjects": None,
                "pdf_subjects_list": None,
                "pdf_role": None,
                "pdf_classes_grades": None,
                "pdf_presence": None,
                "pdf_source_types": None,
                "pdf_source_files": None,
                "pdf_pages": None,
                "pdf_confidence": None,
                "pdf_notes": None,
                "pdf_aliases": None,
                "destination": None,
                "_matched": False,
            })
            continue

        image_type = info.get("image_type", "portrait")

        if image_type == "portrait":
            raw_results.append(info)
            results.append({
                "filename": info["filename"],
                "renamed_filename": None,
                "image_path": str(image_path),
                "card_name": None,
                "card_subject": None,
                "card_ocr_confidence": None,
                "card_uncertain_chars": "",
                "status": "Portrait (no card) — queued for best-shot",
                "match_method": "none",
                "match_score": 0.0,
                "pdf_name": None,
                "pdf_subjects": None,
                "pdf_subjects_list": None,
                "pdf_role": None,
                "pdf_classes_grades": None,
                "pdf_presence": None,
                "pdf_source_types": None,
                "pdf_source_files": None,
                "pdf_pages": None,
                "pdf_confidence": None,
                "pdf_notes": None,
                "pdf_aliases": None,
                "destination": None,
                "_matched": False,
                "_portrait_scores": {
                    "suitability_score": info.get("suitability_score"),
                    "beauty_score": info.get("beauty_score"),
                    "expression_score": info.get("expression_score"),
                    "composite_score": info.get("composite_score"),
                    "is_ng": info.get("is_ng"),
                    "ng_reason": info.get("ng_reason"),
                    "short_comment": info.get("short_comment"),
                },
            })
            continue

        # Card photo.
        conf = float(info.get("confidence") or 0.0)
        unc = info.get("uncertain_chars") or ""

        if not info.get("name"):
            raw_results.append(info)
            results.append({
                "filename": info["filename"],
                "renamed_filename": None,
                "image_path": str(image_path),
                "card_name": None,
                "card_subject": info.get("subject"),
                "card_ocr_confidence": conf,
                "card_uncertain_chars": unc,
                "status": "Card detected but no name readable",
                "match_method": "none",
                "match_score": 0.0,
                "pdf_name": None,
                "pdf_subjects": None,
                "pdf_subjects_list": None,
                "pdf_role": None,
                "pdf_classes_grades": None,
                "pdf_presence": None,
                "pdf_source_types": None,
                "pdf_source_files": None,
                "pdf_pages": None,
                "pdf_confidence": None,
                "pdf_notes": None,
                "pdf_aliases": None,
                "destination": None,
                "_matched": False,
            })
            continue

        match, match_method, match_score = find_match(
            info["name"],
            staff_list,
            card_subject=info.get("subject", ""),
            ocr_confidence=conf,
        )

        card_info = {
            "name": info.get("name"),
            "subject": info.get("subject"),
            "confidence": conf,
            "uncertain_chars": unc,
        }

        if match:
            row = on_match(image_path, card_info, match, match_method, match_score)
        else:
            row = on_no_match(image_path, card_info)

        row["image_type"] = "card"
        # Important: do NOT overwrite row["renamed_filename"] here.
        raw_results.append(info)
        results.append(row)

    # Inject match fields back into raw results for face grouping.
    path_to_result = {r["image_path"]: r for r in results}
    for info in raw_results:
        matched_result = path_to_result.get(info["image_path"], {})
        info["pdf_name"] = matched_result.get("pdf_name")
        info["card_subject"] = matched_result.get("card_subject") or info.get("subject")

    # Step 4: Group portraits by teacher + select / save best shots.
    teacher_groups = group_photos_by_teacher(raw_results, threshold=FACE_MATCH_THRESHOLD)
    best_shot_rows = []
    best_shot_by_key = {}

    for teacher_key, group in teacher_groups.items():
        portraits = group["portrait_photos"]
        if not portraits:
            continue

        card_path = (group.get("card_photos") or [None])[0]
        is_matched = path_to_result.get(card_path or "", {}).get("_matched", False)

        scored = []
        for path in portraits:
            r = path_to_result.get(path, {})
            ps = r.get("_portrait_scores") or {}
            scored.append({
                "filename": Path(path).name,
                "image_path": path,
                "suitability_score": ps.get("suitability_score"),
                "beauty_score": ps.get("beauty_score"),
                "expression_score": ps.get("expression_score"),
                "composite_score": ps.get("composite_score", -1.0),
                "is_ng": ps.get("is_ng", False),
                "ng_reason": ps.get("ng_reason"),
                "short_comment": ps.get("short_comment", ""),
            })

        non_ng = sorted(
            [s for s in scored if not s["is_ng"] and s["composite_score"] >= 0],
            key=lambda x: x["composite_score"],
            reverse=True,
        )
        ng = [s for s in scored if s["is_ng"] or s["composite_score"] < 0]
        scored = non_ng + ng

        best_path, rows = save_portrait_shots(teacher_key, group, scored, path_to_result)
        best_shot_rows.extend(rows)
        best_shot_by_key[teacher_key] = best_path

        for bsr in rows:
            orig_path = bsr["image_path"]
            if orig_path in path_to_result:
                path_to_result[orig_path]["renamed_filename"] = bsr["renamed_filename"]
                path_to_result[orig_path]["destination"] = str(
                    (Path(BEST_SHOT_FOLDER) if is_matched else Path(UNKNOWN_FOLDER)) / bsr["renamed_filename"]
                )

    # Step 5: Save Excel report.
    save_excel(results, _pdf_staff_master, EXCEL_OUTPUT)
    wb = openpyxl.load_workbook(EXCEL_OUTPUT)
    add_best_shot_sheet(wb, best_shot_rows)
    add_staff_by_subject_sheet(wb, _pdf_staff_master)
    wb.save(EXCEL_OUTPUT)

    output_zip = _make_zip(output_dir, output_dir / "output.zip")

    n_cards = sum(1 for r in raw_results if r.get("image_type") == "card")
    n_portraits = sum(1 for r in raw_results if r.get("image_type") == "portrait")
