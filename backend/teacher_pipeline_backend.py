def postprocess_record(r: dict, filename: str, page_num=None):
    surname    = normalize_whitespace(r.get("surname", ""))
    given_name = normalize_whitespace(r.get("given_name", ""))
    teacher_name = clean_teacher_name(r.get("teacher_name", ""))

    if not teacher_name:
        if surname and given_name:
            teacher_name = clean_teacher_name(f"{surname} {given_name}")
        elif surname:
            teacher_name = clean_teacher_name(surname)
        elif given_name:
            teacher_name = clean_teacher_name(given_name)

    role_tags = []
    for tag in r.get("role_tags", []) or []:
        tag2 = normalize_whitespace(tag)
        if tag2 and tag2 not in role_tags:
            role_tags.append(tag2)

    subjects = sort_subjects(r.get("subjects", []))
    for tag in role_tags:
        tag_clean = clean_subject_token(tag)
        if tag_clean in ROLE_SUBJECTS and tag_clean not in subjects:
            subjects.append(tag_clean)
    subjects = sort_subjects(subjects)

    classes_or_grades = []
    for x in r.get("classes_or_grades", []) or []:
        x2 = normalize_whitespace(x)
        if x2 and x2 not in classes_or_grades:
            classes_or_grades.append(x2)

    try:
        confidence = float(r.get("confidence", 0.0))
    except Exception:
        confidence = 0.0

    source_type = r.get("source_type") or "roster"
    if source_type not in {"roster", "timetable", "previous_layout", "other"}:
        source_type = "roster"

    return {
        "teacher_name":     teacher_name,
        "subjects":         subjects,
        "role_tags":        role_tags,
        "classes_or_grades": classes_or_grades,
        "confidence":       confidence,
        "source_type":      source_type,
        "notes":            normalize_whitespace(r.get("notes", "")),
        "__file":           filename,
        "__page_num":       page_num,
    }

