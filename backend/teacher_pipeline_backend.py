def _subject_prefix(pdf_subject: str, card_subject: str, pdf_role: str) -> str:
    """
    Build the subject/role prefix for the filename.
    Priority: PDF subject → PDF role → card OCR subject → '不明'
    Appends 科 unless the token is a role (校長 etc.) or already ends with 科.
    """
    token = ""

    if pdf_subject:
        parts = [p.strip() for p in pdf_subject.split(",") if p.strip()]
        if parts:
            token = parts[0]

    if not token and pdf_role:
        parts = [p.strip() for p in pdf_role.split(",") if p.strip()]
        if parts:
            token = parts[0]

    if not token and card_subject:
        token = clean_subject_token(card_subject)

    if not token:
        return "不明"

    token = clean_subject_token(token)

    if token in ROLE_SUBJECTS:
        return token          # e.g. 校長, 教頭 — no 科 suffix

    if token.endswith("科"):
        return token

    return token + "科"       # e.g. 国語 → 国語科


def _safe_name(name: str) -> str:
    """Strip spaces and filesystem-unsafe characters from a name."""
    if not name:
        return "不明"
    name = re.sub(r"[\s\u3000/\\:*?\"<>|]+", "", name)
    return name or "不明"


def _stem(filename: str) -> str:
    """'13670113.JPG' → '13670113'"""
    return Path(filename).stem


def build_filename(
    subject_prefix: str,
    teacher_name: str,
    original_filename: str,
    tag: str,    # '札' or '本'
    seq: int,    # 1-based
    ext: str,
) -> str:
    """Returns e.g. '国語科_高橋次郎_13670113_札01.jpg'"""
    return (
        f"{subject_prefix}"
        f"_{_safe_name(teacher_name)}"
        f"_{_stem(original_filename)}"
        f"_{tag}{seq:02d}"
        f"{ext.lower()}"
    )

