# PATCH 3 — find_match  (confidence-gated + surname-locked fuzzy)
#
# New behaviour:
#  • If OCR confidence >= 0.90: fuzzy threshold = 0.72 (same as v4)
#  • If OCR confidence 0.70–0.89: threshold = 0.72 BUT surname must match
#    (prevents 細田→飛田 type false positives where surname is wrong)
#  • If OCR confidence < 0.70: threshold = 0.82 (stricter, fewer false matches)
#  • Subject-boosted pass retained at 0.60 (helps katakana names like フクダ)
#
# Also: surname_matches() helper — the surname is the MORE reliable part of a
# fuzzy match. If OCR confidence is medium, require surnames to share >= 0.85
# similarity before accepting a fuzzy match.
# ─────────────────────────────────────────────────────────────────────────────

def _split_surname(name: str) -> str:
    """
    Return just the surname part of a Japanese name for comparison.
    Heuristic: everything before the first space (or first 2 chars if no space).
    """
    name = name.strip()
    if " " in name or "\u3000" in name:
        return re.split(r"[\s\u3000]+", name)[0]
    # No space — assume first 1–2 chars are surname
    return name[:2] if len(name) >= 4 else name[:1]

def _surname_sim(a: str, b: str) -> float:
    sa = normalize_teacher_key(_split_surname(a))
    sb = normalize_teacher_key(_split_surname(b))
    if not sa or not sb:
        return 0.0
    return SequenceMatcher(None, sa, sb).ratio()

def _get_staff_subjects(staff: dict) -> list:
    return [clean_subject_token(x) for x in (staff.get("_subjects") or [])]

def _subject_matches(card_subject: str, staff: dict) -> bool:
    card_clean = clean_subject_token(card_subject or "")
    if not card_clean:
        return False
    ss = _get_staff_subjects(staff)
    return card_clean in ss or any(card_clean in x or x in card_clean for x in ss)


def _subject_matches_broad(card_subject: str, staff: dict) -> bool:
    """
    Extended subject matching that checks both _subjects list AND the role field.
    This catches cases like card subject "教頭" matching a staff entry where
    教頭 is stored in the "role" field rather than "_subjects".
    Also handles the 教愛→教頭 alias.
    """
    card_clean = clean_subject_token(card_subject or "")
    if not card_clean:
        return False

    # Check academic subjects list
    ss = [clean_subject_token(x) for x in (staff.get("_subjects") or [])]
    if card_clean in ss or any(card_clean in x or x in card_clean for x in ss):
        return True

    # Also check role field (e.g. 教頭, 校長, 主事 stored there)
    role_str = staff.get("role", "") or ""
    role_tokens = [clean_subject_token(r.strip()) for r in role_str.split(",") if r.strip()]
    if card_clean in role_tokens or any(card_clean in r or r in card_clean for r in role_tokens):
        return True

    return False


def find_match(
    extracted_name: str,
    staff_list: list,
    card_subject: str = "",
    ocr_confidence: float = 1.0,
):
    """
    v8  —  adds Pass 4c (fuzzy-surname + subject) for low-confidence OCR.

    match_method values:
        "exact"                — normalised keys identical
        "partial"              — one key contains the other
        "fuzzy"                — SequenceMatcher ratio above threshold
        "surname_subject"      — exact surname key + subject overlap
        "subject_fuzzy"        — subject overlap + fuzzy above 0.60
        "fuzzy_surname_subject"— NEW: fuzzy surname (≥0.60) + subject, OCR conf < 0.75
        "none"                 — no match found
    """
    norm = normalize_teacher_key(extracted_name)
    if not norm:
        return None, "none", 0.0

    # Confidence-gated thresholds (unchanged from v7)
    if ocr_confidence >= 0.70:
        fuzzy_thresh   = 0.72
        surname_thresh = 0.0
    else:
        fuzzy_thresh   = 0.80
        surname_thresh = 0.85

    raw_norm = re.sub(r"[\s\u3000]+", "", extracted_name)

    # ── Pass 1 — exact ────────────────────────────────────────────────────────
    for staff in staff_list:
        sn = re.sub(r"[\s\u3000]+", "", staff.get("name", ""))
        if normalize_teacher_key(staff.get("name", "")) == norm or sn == raw_norm:
            sim = name_similarity(extracted_name, staff.get("name", ""))
            return staff, "exact", round(sim, 4)

    # ── Pass 2 — partial containment ─────────────────────────────────────────
    for staff in staff_list:
        sn = normalize_teacher_key(staff.get("name", ""))
        if sn and (sn in norm or norm in sn):
            sim = name_similarity(extracted_name, staff.get("name", ""))
            return staff, "partial", round(sim, 4)

    # ── Score all candidates ──────────────────────────────────────────────────
    scored = sorted(
        [(name_similarity(extracted_name, s.get("name", "")), s) for s in staff_list],
        key=lambda x: -x[0],
    )
    if not scored:
        return None, "none", 0.0

    best_sim, best_staff = scored[0]

    # ── Pass 3 — pure fuzzy ───────────────────────────────────────────────────
    if best_sim >= fuzzy_thresh:
        sur_sim = _surname_sim(extracted_name, best_staff.get("name", ""))
        if surname_thresh == 0.0 or sur_sim >= surname_thresh:
            return best_staff, "fuzzy", round(best_sim, 4)

    # ── Pass 4a — surname exact + subject match ───────────────────────────────
    card_surname_key = normalize_teacher_key(_split_surname(extracted_name))
    card_given       = norm[len(card_surname_key):]

    if card_surname_key and len(card_surname_key) >= 1:
        surname_candidates = [
            (sim, staff) for sim, staff in scored[:15]
            if normalize_teacher_key(_split_surname(staff.get("name", ""))) == card_surname_key
               and _subject_matches_broad(card_subject, staff)
               and sim >= 0.30
        ]
        for sim, staff in surname_candidates:
            staff_given = normalize_teacher_key(
                staff.get("name", "")
            )[len(normalize_teacher_key(_split_surname(staff.get("name", "")))):]
            if card_given and staff_given:
                shared_chars = set(card_given) & set(staff_given)
                if len(card_given) == 1 and len(staff_given) == 1 and not shared_chars:
                    continue
            return staff, "surname_subject", round(sim, 4)

    # ── Pass 4b — subject-boosted fuzzy (threshold 0.60) ─────────────────────
    for sim, staff in scored[:5]:
        if sim >= 0.60 and _subject_matches_broad(card_subject, staff):
            sur_sim = _surname_sim(extracted_name, staff.get("name", ""))
            if sur_sim >= 0.75:
                return staff, "subject_fuzzy", round(sim, 4)

    # ── Pass 4c — fuzzy surname + subject  [NEW in v8] ───────────────────────
    # Activated only when OCR confidence is low (< 0.75).
    # Useful when the OCR misreads one character of the surname but the subject
    # and the overall name similarity still point to the right person.
    #
    # Requirements (all must be true):
    #   • ocr_confidence < 0.75
    #   • surname similarity >= 0.60  (shares at least the first kanji)
    #   • subject overlap (broad: includes role field)
    #   • overall name similarity >= 0.40  (avoids completely unrelated names)
    #   • the candidate must be ranked in top-10 by overall similarity
    #
    # Why these thresholds?
    #   sur_sim 0.60 = SequenceMatcher on 1–2 char keys, so "安齋"→"安藤" scores
    #   ~0.67 (share the first kanji 安).  0.60 is the minimum that still
    #   excludes unrelated surnames while allowing one-kanji-off misreads.
    #   Overall sim 0.40 ensures we are not matching completely different people
    #   just because they teach the same subject.
    if ocr_confidence < 0.80:
        for sim, staff in scored[:10]:
            if sim < 0.40:
                break                                   # sorted descending — no point continuing
            sur_sim = _surname_sim(extracted_name, staff.get("name", ""))
            if sur_sim >= 0.60 and _subject_matches_broad(card_subject, staff):
                return staff, "fuzzy_surname_subject", round(sim, 4)

    return None, "none", 0.0


