def normalize_teacher_key(name: str) -> str:
    """
    Produce a normalised matching key from a Japanese teacher name.
    Handles: spacing, old/new kanji variants, katakana→hiragana,
    okurigana-style kana in names, noise tokens.
    """
    if not name:
        return ""

    # ── basic clean ───────────────────────────────────────────────────────
    name = str(name)
    name = re.sub(r"[：:]+$", "", name)
    name = re.sub(r"^(教員|教諭|講師|先生)\s*", "", name)
    name = re.sub(r"\s*先生$", "", name)

    # ── collapse ALL whitespace / special spaces ───────────────────────────
    # Handles: U+0020, U+3000 (ideographic), U+00A0 (NBSP), U+200B (ZWSP)
    for sp in ["\u3000", "\u00a0", "\u200b", "\u200c", "\u200d", "\ufeff"]:
        name = name.replace(sp, "")
    name = re.sub(r"\s+", "", name)

    # ── remove parenthetical readings ─────────────────────────────────────
    name = re.sub(r"（[^）]*）", "", name)
    name = re.sub(r"\([^)]*\)", "", name)

    # ── apply kanji variant table ──────────────────────────────────────────
    for old, new in _KANJI_VARIANTS.items():
        name = name.replace(old, new)

    # ── NFC normalisation (collapses many compatibility variants) ──────────
    name = unicodedata.normalize("NFC", name)

    # ── katakana → hiragana (so フクダ ~ ふくだ) ──────────────────────────
    name = katakana_to_hiragana(name)

    # ── strip noise tokens ─────────────────────────────────────────────────
    name = re.sub(
        r"(名不鮮明|姓不鮮明|判読不明|不鮮明|不明|暫定|推定|可能性|先生|教諭|講師|教員)",
        "",
        name,
    )

    return name.strip()

def name_similarity(a: str, b: str) -> float:
    a2 = normalize_teacher_key(a)
    b2 = normalize_teacher_key(b)
    if not a2 or not b2:
        return 0.0
    return SequenceMatcher(None, a2, b2).ratio()

