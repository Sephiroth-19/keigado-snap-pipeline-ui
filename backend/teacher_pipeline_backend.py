def _write_header_row(ws, headers: list, bg_hex: str = "2C3E50"):
    fill = PatternFill("solid", fgColor=bg_hex)
    font = Font(color="FFFFFF", bold=True)
    for col, h in enumerate(headers, start=1):
        cell            = ws.cell(row=1, column=col, value=h)
        cell.fill       = fill
        cell.font       = font
        cell.alignment  = Alignment(horizontal="center", wrap_text=True)

def _autofit(ws, max_width: int = 55):
    for col in ws.columns:
        max_len = max((len(str(cell.value or "")) for cell in col), default=0)
        ws.column_dimensions[col[0].column_letter].width = min(max_len + 4, max_width)

def _freeze(ws, cell_ref: str = "A2"):
    ws.freeze_panes = cell_ref

