def safe_json_object_load(raw: str, default=None):
    if default is None:
        default = {}
    raw = (raw or "").strip()
    if not raw:
        return default
    try:
        return json.loads(raw)
    except Exception:
        pass
    m = re.search(r"\{.*\}", raw, flags=re.S)
    if m:
        try:
            return json.loads(m.group(0))
        except Exception:
            pass
    return default

