def _call_unified_once(image_path: str, prompt: str) -> dict:
    """Single GPT vision call with the unified prompt. Returns parsed dict."""
    ext  = Path(image_path).suffix.lower().lstrip(".")
    mime = "image/jpeg" if ext in ("jpg", "jpeg") else f"image/{ext}"
    b64  = encode_image_file(image_path)
    response = client.chat.completions.create(
        model=MODEL_VISION,
        max_completion_tokens=500,
        messages=[{
            "role": "user",
            "content": [
                {"type": "text",      "text": prompt},
                {"type": "image_url", "image_url": {"url": f"data:{mime};base64,{b64}"}},
            ],
        }],
    )
    raw = response.choices[0].message.content
    try:
        result = parse_json(raw)
        return result
    except Exception as e:
        return {"error": str(e), "raw": raw}


def extract_image_info(image_path: str) -> dict:
    """
    Single GPT call per image.

    Returns a unified dict:
      {
        "image_type":      "card" | "portrait",

        # card fields (present when image_type == "card")
        "name":            str,
        "subject":         str,
        "confidence":      float,
        "uncertain_chars": str,

        # portrait fields (present when image_type == "portrait")
        "suitability_score": int,
        "beauty_score":      int,
        "expression_score":  int,
        "composite_score":   float,
        "is_ng":             bool,
        "ng_reason":         str | None,
        "short_comment":     str,

        # retry metadata (optional)
        "_retry_used":       bool,
      }
    """
    print(f"  [image] {Path(image_path).name}")

    result = _call_unified_once(image_path, UNIFIED_PROMPT)

    if "error" in result:
        return result

    image_type = result.get("image_type", "portrait")

    # ── Card photo: apply retry logic if confidence is low ────────────────────
    if image_type == "card":
        card = result.get("card") or {}
        conf = float(card.get("confidence") or 0.0)

        if conf < LOW_CONF_THRESHOLD:
            print(f"    ↳ low confidence ({conf:.2f}), retrying…")
            addendum = _UNIFIED_RETRY_ADDENDUM.format(
                prev_name    = card.get("name", ""),
                prev_subject = card.get("subject", ""),
                uncertain    = card.get("uncertain_chars", "") or "none flagged",
            )
            result2 = _call_unified_once(image_path, UNIFIED_PROMPT + "\n\n" + addendum)

            if "error" not in result2:
                card2  = result2.get("card") or {}
                conf2  = float(card2.get("confidence") or 0.0)
                print(f"    ↳ retry confidence: {conf2:.2f}  (first: {conf:.2f})")
                if conf2 >= conf:
                    result2["_retry_used"]      = True
                    result2["_first_read_name"] = card.get("name", "")
                    result2["_first_read_conf"] = conf
                    result = result2
                else:
                    result["_retry_used"]       = True
                    result["_retry_read_name"]  = card2.get("name", "")
                    result["_retry_read_conf"]  = conf2

        # Flatten card fields to top level for compatibility with find_match()
        card = result.get("card") or {}
        result["name"]            = card.get("name", "")
        result["subject"]         = card.get("subject", "")
        result["confidence"]      = float(card.get("confidence") or 0.0)
        result["uncertain_chars"] = card.get("uncertain_chars", "") or ""

    # ── Portrait photo: compute composite score ───────────────────────────────
    elif image_type == "portrait":
        portrait = result.get("portrait") or {}
        is_ng    = bool(portrait.get("is_ng"))
        if is_ng:
            composite = -1.0
        else:
            s = float(portrait.get("suitability_score") or 0)
            b = float(portrait.get("beauty_score")      or 0)
            e = float(portrait.get("expression_score")  or 0)
            composite = s * 0.50 + b * 0.30 + e * 0.20

        result["suitability_score"] = portrait.get("suitability_score")
        result["beauty_score"]      = portrait.get("beauty_score")
        result["expression_score"]  = portrait.get("expression_score")
        result["composite_score"]   = round(composite, 4)
        result["is_ng"]             = is_ng
        result["ng_reason"]         = portrait.get("ng_reason")
        result["short_comment"]     = portrait.get("short_comment", "")

    return result


