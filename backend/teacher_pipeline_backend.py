def extract_staff_from_pdf(pdf_path: str) -> list:
    """
    Advanced PDF roster extraction using the structured-row pipeline.
    Returns a flat list of dicts, each with at least {"name": ..., "subjects": [...], "role": ...}
    for compatibility with the card-matcher find_match() function.
    """
    print(f"  [pdf] Reading: {Path(pdf_path).name}")

    with open(pdf_path, "rb") as f:
        pdf_bytes = f.read()

    filename    = Path(pdf_path).name
    source_kind = "roster"
    all_records = []

    # ── Try structured row extraction first ──
    structured_pages = pdf_extract_roster_structured_rows(pdf_bytes, y_tol=4.0)
    total_rows = sum(len(p["rows"]) for p in structured_pages)
    print(f"  [pdf] Structured row extraction: {total_rows} raw rows across {len(structured_pages)} page(s)")

    if total_rows >= 8:
        for page in structured_pages:
            page_num  = page["page_num"]
            page_rows = page["rows"]
            if not page_rows:
                continue
            print(f"  [pdf] Page {page_num}: {len(page_rows)} rows → GPT row-repair...")
            recs = call_gpt_roster_structured_rows_page(filename, page_num, page_rows)
            print(f"         → {len(recs)} records returned")
            for r in recs:
                if not isinstance(r, dict):
                    continue
                pr = postprocess_record(r, filename, page_num)
                if pr["teacher_name"]:
                    all_records.append(pr)
    else:
        # ── Fallback: render pages as images → vision ──
        print("  [pdf] Too few text rows; falling back to vision extraction...")
        page_images = pdf_render_page_images_bytes(pdf_bytes, max_pages=100, zoom=2.2)
        for page_num, img in page_images:
            print(f"  [pdf] Vision page {page_num}/{len(page_images)} → GPT...")
            obj  = call_gpt_vision_extract_page(filename, page_num, img, source_kind)
            recs = obj.get("records", []) or []
            print(f"         → {len(recs)} records returned")
            for r in recs:
                if not isinstance(r, dict):
                    continue
                pr = postprocess_record(r, filename, page_num)
                if pr["teacher_name"]:
                    all_records.append(pr)

    print(f"  [pdf] Raw records before merge: {len(all_records)}")

    # ── Merge / dedupe ──
    master = merge_master(all_records)
    teachers = master  # finalize_entities returns a list directly

    print(f"  [pdf] Merged teachers: {len(teachers)}")

    # ── Flatten to the list used by find_match(), keeping ALL pipeline fields ──
    staff_list = []
    for t in teachers:
        name = t.get("teacher_name", "")
        if not name:
            continue
        subjects       = t.get("subjects", []) or []
        classes_grades = t.get("classes_or_grades", []) or []
        source_files   = t.get("source_files", []) or []
        source_types   = t.get("source_types", []) or []
        pages          = t.get("pages", []) or []
        presence       = t.get("presence", "")
        max_confidence = t.get("max_confidence", 0.0)
        notes          = t.get("notes", "") or ""

        # Aliases were embedded in the notes field by finalize_entities
        aliases = ""
        alias_match = re.search(r"Merged aliases:\s*(.+)", notes)
        if alias_match:
            aliases = alias_match.group(1).strip()
            notes   = notes[:alias_match.start()].strip()

        # Separate role-type entries from academic subjects for cleaner display
        role_subjects = [s for s in subjects if s in ROLE_SUBJECTS]
        pure_subjects = [s for s in subjects if s not in ROLE_SUBJECTS]

        staff_list.append({
            # ── used by find_match() ──
            "name":               name,
            "subject":            ", ".join(pure_subjects) if pure_subjects else ", ".join(subjects),
            "role":               ", ".join(role_subjects),
            # ── full detail, prefixed _ for Excel sheet use ──
            "_subjects":          subjects,
            "_pure_subjects":     pure_subjects,
            "_role_subjects":     role_subjects,
            "_classes_or_grades": ", ".join(classes_grades),
            "_source_files":      ", ".join(source_files),
            "_source_types":      ", ".join(source_types),
            "_pages":             ", ".join(map(str, pages)),
            "_presence":          presence,
            "_confidence":        round(max_confidence, 4),
            "_notes":             notes,
            "_aliases":           aliases,
        })

    # Store full master list for the PDF Staff Master sheet in Excel
    global _pdf_staff_master
    _pdf_staff_master = teachers

    return staff_list

