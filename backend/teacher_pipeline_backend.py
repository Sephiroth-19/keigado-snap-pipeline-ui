# Katakana→Hiragana offset
_KT2HG = 0x60  # ord('ア') - ord('あ') = 96

def katakana_to_hiragana(s: str) -> str:
    """Convert full-width katakana to hiragana for fuzzy key comparison."""
    result = []
    for ch in s:
        cp = ord(ch)
        if 0x30A1 <= cp <= 0x30F6:          # ァ–ヶ
            result.append(chr(cp - _KT2HG))
        else:
            result.append(ch)
    return "".join(result)

