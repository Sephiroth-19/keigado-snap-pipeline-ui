def pdf_render_page_images_bytes(b: bytes, max_pages=100, zoom=2.0):
    doc = fitz.open(stream=b, filetype="pdf")
    images = []
    n = min(len(doc), max_pages)
    for i in range(n):
        page = doc[i]
        mat  = fitz.Matrix(zoom, zoom)
        pix  = page.get_pixmap(matrix=mat)
        img  = Image.open(io.BytesIO(pix.tobytes("png"))).convert("RGB")
        images.append((i + 1, img))
    doc.close()
    return images

def pdf_extract_words_by_pages_bytes(b: bytes):
    doc = fitz.open(stream=b, filetype="pdf")
    pages = []
    for i in range(len(doc)):
        page = doc[i]
        words = page.get_text("words")
        pages.append({
            "page_num":    i + 1,
            "page_width":  float(page.rect.width),
            "page_height": float(page.rect.height),
            "words":       words or [],
        })
    doc.close()
    return pages

def group_words_into_rows(words, y_tol=4.0):
    if not words:
        return []
    words_sorted = sorted(words, key=lambda w: (((w[1] + w[3]) / 2.0), w[0]))
    rows, current_group, current_y = [], [], None
    for w in words_sorted:
        yc = (w[1] + w[3]) / 2.0
        if current_y is None:
            current_group, current_y = [w], yc
        elif abs(yc - current_y) <= y_tol:
            current_group.append(w)
            current_y = (current_y * (len(current_group) - 1) + yc) / len(current_group)
        else:
            rows.append(current_group)
            current_group, current_y = [w], yc
    if current_group:
        rows.append(current_group)
    normalised = []
    for idx, row in enumerate(rows, start=1):
        row_sorted = sorted(row, key=lambda w: w[0])
        tokens = [{"text": str(w[4]), "x0": float(w[0]), "x1": float(w[2]),
                   "y0": float(w[1]), "y1": float(w[3])} for w in row_sorted]
        normalised.append({
            "row_num":  idx,
            "tokens":   tokens,
            "row_text": " ".join(t["text"] for t in tokens).strip(),
        })
    return normalised

def estimate_roster_column_boundaries(page_width: float):
    return {
        "subject": (0.00 * page_width, 0.17 * page_width),
        "role":    (0.17 * page_width, 0.41 * page_width),
        "grade":   (0.41 * page_width, 0.54 * page_width),
        "surname": (0.54 * page_width, 0.73 * page_width),
        "given":   (0.73 * page_width, 0.98 * page_width),
    }

def collect_tokens_in_xrange(tokens, xmin, xmax):
    picked = [t["text"] for t in tokens if xmin <= (t["x0"] + t["x1"]) / 2.0 < xmax]
    return normalize_whitespace(" ".join(picked))

def rows_to_structured_cells(rows, page_width):
    bounds = estimate_roster_column_boundaries(page_width)
    structured = []
    for row in rows:
        row_text = normalize_whitespace(row["row_text"])
        if len(re.sub(r"\s+", "", row_text)) < 3:
            continue
        tokens = row["tokens"]
        structured.append({
            "row_num":        row["row_num"],
            "row_text":       row_text,
            "subject_raw":    collect_tokens_in_xrange(tokens, *bounds["subject"]),
            "role_raw":       collect_tokens_in_xrange(tokens, *bounds["role"]),
            "grade_raw":      collect_tokens_in_xrange(tokens, *bounds["grade"]),
            "surname_raw":    collect_tokens_in_xrange(tokens, *bounds["surname"]),
            "given_name_raw": collect_tokens_in_xrange(tokens, *bounds["given"]),
            "tokens":         tokens,
        })
    return structured

def pdf_extract_roster_structured_rows(b: bytes, y_tol=4.0):
    pages_words = pdf_extract_words_by_pages_bytes(b)
    out = []
    for p in pages_words:
        rows = group_words_into_rows(p["words"], y_tol=y_tol)
        structured = rows_to_structured_cells(rows, p["page_width"])
        out.append({
            "page_num":    p["page_num"],
            "page_width":  p["page_width"],
            "page_height": p["page_height"],
            "rows":        structured,
        })
    return out

def to_data_url(pil_img: Image.Image) -> str:
    buf = io.BytesIO()
    pil_img.save(buf, format="PNG")
    return "data:image/png;base64," + base64.b64encode(buf.getvalue()).decode("utf-8")

