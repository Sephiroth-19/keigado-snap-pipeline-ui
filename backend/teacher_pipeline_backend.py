def save_portrait_shots(
    teacher_key: str,
    group: dict,
    scored: list,          # already sorted: non-NG desc, then NG
    path_to_result: dict,
) -> tuple:
    """
    Saves ALL portraits for one teacher, ranked 本01, 本02 …
    Destination:
      matched teacher   → BestShot/
      unmatched teacher → Unknown/

    Returns (best_path_or_None, list_of_best_shot_row_dicts)
    """
    # Look up the card match row to get naming fields
    card_image_path = (group.get("card_photos") or [None])[0]
    match_row       = path_to_result.get(card_image_path or "", {})
    is_matched      = match_row.get("_matched", False)

    subj_prefix  = match_row.get("_subject_prefix") or _subject_prefix(
        match_row.get("pdf_subjects", ""),
        group.get("card_subject", ""),
        match_row.get("pdf_role", ""),
    )
    teacher_name = match_row.get("_teacher_name") or group.get("pdf_name") or teacher_key

    dest_folder  = Path(BEST_SHOT_FOLDER) if is_matched else Path(UNKNOWN_FOLDER)

    best_path      = None
    best_shot_rows = []

    for seq, sd in enumerate(scored, start=1):
        src_path = sd["image_path"]
        ext      = Path(src_path).suffix

        new_name = build_filename(subj_prefix, teacher_name, sd["filename"], "本", seq, ext)
        dest     = dest_folder / new_name
        shutil.copy2(src_path, dest)

        is_best = (seq == 1 and not sd.get("is_ng"))
        if is_best:
            best_path = src_path

        tag = "✅ Best" if is_best else ("⛔ NG " if sd.get("is_ng") else f"     #{seq} ")
        folder_label = "BestShot" if is_matched else "Unknown"
        print(f"    {tag}  {folder_label}/{new_name}  (composite={sd.get('composite_score', 0.0):.2f})")

        best_shot_rows.append({
            "teacher_key":       teacher_key,
            "pdf_name":          group.get("pdf_name") or teacher_key,
            "card_subject":      group.get("card_subject", ""),
            "filename":          sd["filename"],
            "renamed_filename":  new_name,
            "image_path":        src_path,
            "rank":              seq,
            "suitability_score": sd.get("suitability_score"),
            "beauty_score":      sd.get("beauty_score"),
            "expression_score":  sd.get("expression_score"),
            "composite_score":   sd.get("composite_score"),
            "is_ng":             sd.get("is_ng"),
            "ng_reason":         sd.get("ng_reason"),
            "short_comment":     sd.get("short_comment", ""),
            "is_selected_best":  is_best,
            "destination_folder": folder_label,
        })

    return best_path, best_shot_rows


