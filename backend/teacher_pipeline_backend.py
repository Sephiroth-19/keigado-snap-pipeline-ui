# ── Cell 6: Text-normalisation helpers ────────────────────────────────────────

def normalize_whitespace(s: str) -> str:
    s = str(s or "")
    s = s.replace("\u3000", " ")
    s = re.sub(r"\s+", " ", s).strip()
    return s

def clean_teacher_name(name: str) -> str:
    name = normalize_whitespace(name)
    name = re.sub(r"[：:]+$", "", name)
    name = re.sub(r"^(教員|教諭|講師|先生)\s*", "", name)
    name = re.sub(r"\s*先生$", "", name)
    return name.strip()

def clean_subject_token(subj: str) -> str:
    subj = normalize_whitespace(subj)
    subj = re.sub(r"[①②③④⑤⑥⑦⑧⑨⑩]", "", subj)
    subj = re.sub(r"[0-9０-９]+$", "", subj)
    subj = re.sub(r"\s+", "", subj)
    subj = SUBJECT_ALIASES.get(subj, subj)
    return subj

def subject_sort_key(subj: str):
    subj = clean_subject_token(subj)
    if subj in SUBJECT_SORT_ORDER:
        return (0, SUBJECT_SORT_ORDER.index(subj), subj)
    return (1, 9999, subj)

def sort_subjects(subjects):
    uniq = []
    seen = set()
    for s in subjects or []:
        s2 = clean_subject_token(s)
        if not s2:
            continue
        if s2 not in seen:
            uniq.append(s2)
            seen.add(s2)
    return sorted(uniq, key=subject_sort_key)


