def fill_manual_entries(results: list, output_path: str):
    """
    For every unmatched photo: displays the image in a dedicated output
    widget (forces immediate render), then prompts for the correct name.
    Updates 'Final Name List' sheet and re-saves Excel.
    """
    unmatched = [
        (idx, r) for idx, r in enumerate(results)
        if "manual review" in r.get("status", "")
    ]

    if not unmatched:
        print("  ✔ No unmatched photos — nothing to review manually.")
        return

    print(f"\n{'=' * 60}")
    print(f"Manual Review — {len(unmatched)} unmatched photo(s)")
    print(f"{'=' * 60}")
    print("The photo will appear above each prompt.")
    print("Type the correct name and press Enter.")
    print("Press Enter with no input to skip.")
    print("Type 'q' to stop early.\n")
    sys.stdout.flush()

    wb  = openpyxl.load_workbook(output_path)
    ws3 = wb[TEACHER_SHEET_LABELS["Final Name List"]]
    green = PatternFill("solid", fgColor="D5F5E3")

    # filename → sheet row lookup
    filename_to_row = {}
    for row in ws3.iter_rows(min_row=2, values_only=False):
        cell_val = row[0].value
        if cell_val:
            filename_to_row[str(cell_val)] = row[0].row

    filled = 0
    total  = len(unmatched)

    for counter, (list_idx, row) in enumerate(unmatched, start=1):
        filename   = row.get("filename", "")
        image_path = row.get("image_path", "")
        card_name  = row.get("card_name") or "(unreadable)"
        card_subj  = row.get("card_subject") or ""

        print(f"{'─' * 60}")
        print(f"  [{counter}/{total}]  {filename}")
        print(f"  OCR read : {card_name}  |  subject: {card_subj}")
        sys.stdout.flush()

        # ── Display image in its own Output widget ────────────────────────────
        # widgets.Output forces immediate rendering independent of input()
        img_out = widgets.Output()
        with img_out:
            if image_path and Path(image_path).exists():
                try:
                    display(IPImage(filename=image_path, width=600))
                except Exception as e:
                    print(f"  ⚠ Could not display image: {e}")
            else:
                print(f"  ⚠ Image not found: {image_path}")

        # Render the widget — this flushes immediately before input() blocks
        display(img_out)

        # Small sleep ensures the widget render pipeline completes in Colab
        # before the kernel switches to stdin-wait mode
        time.sleep(0.5)
        sys.stdout.flush()

        # ── Input prompt ──────────────────────────────────────────────────────
        user_input = input("  Correct name (Enter to skip, q to quit): ").strip()

        if user_input.lower() in ("q", "quit"):
            print("  Stopping early — saving what has been entered.")
            break

        if not user_input:
            print("  → Skipped.\n")
            continue

        # ── Write to Sheet 3 ──────────────────────────────────────────────────
        sheet_row = filename_to_row.get(filename)
        if sheet_row:
            ws3.cell(row=sheet_row, column=2, value=user_input).fill = green
            ws3.cell(row=sheet_row, column=4, value="manual").fill = green
            ws3.cell(row=sheet_row, column=9,
                     value="Manually entered by operator").fill = green
            filled += 1
            print(f"  → Saved: {user_input}\n")
        else:
            print(f"  ⚠ Row not found for {filename} in Sheet 3 — skipped.\n")

    wb.save(output_path)
    print(f"{'=' * 60}")
    print(f"  ✔ Manual review complete — {filled} name(s) filled in.")
    print(f"  📊 Excel updated → {output_path}")
    print(f"{'=' * 60}\n")


