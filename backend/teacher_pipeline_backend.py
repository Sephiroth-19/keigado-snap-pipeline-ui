def build_entity_from_record(r: dict):
    return {
        "teacher_name":      clean_teacher_name(r.get("teacher_name", "")),
        "teacher_name_key":  normalize_teacher_key(r.get("teacher_name", "")),
        "subjects":          sort_subjects(r.get("subjects", []) or []),
        "classes_or_grades": sorted(list(set(r.get("classes_or_grades", []) or []))),
        "source_files":      sorted(list(set([r["__file"]] if r.get("__file") else []))),
        "source_types":      sorted(list(set([r.get("source_type", "other")]))),
        "pages":             sorted(list(set([r["__page_num"]] if r.get("__page_num") else []))),
        "max_confidence":    float(r.get("confidence", 0.0) or 0.0),
        "notes_list":        [normalize_whitespace(r.get("notes", ""))] if normalize_whitespace(r.get("notes", "")) else [],
        "aliases":           [],
    }

def merge_record_into_entity(ent: dict, r: dict, keep_roster_name=True):
    rec_name = clean_teacher_name(r.get("teacher_name", ""))
    if not keep_roster_name:
        if len(normalize_teacher_key(rec_name)) > len(normalize_teacher_key(ent["teacher_name"])):
            ent["teacher_name"]     = rec_name
            ent["teacher_name_key"] = normalize_teacher_key(rec_name)

    for s in sort_subjects(r.get("subjects", []) or []):
        if s not in ent["subjects"]:
            ent["subjects"].append(s)
    ent["subjects"] = sort_subjects(ent["subjects"])

    for cg in r.get("classes_or_grades", []) or []:
        cg2 = normalize_whitespace(cg)
        if cg2 and cg2 not in ent["classes_or_grades"]:
            ent["classes_or_grades"].append(cg2)
    ent["classes_or_grades"] = sorted(ent["classes_or_grades"])

    src_file = r.get("__file", "")
    if src_file and src_file not in ent["source_files"]:
        ent["source_files"].append(src_file)
    ent["source_files"] = sorted(ent["source_files"])

    src_type = r.get("source_type", "other")
    if src_type and src_type not in ent["source_types"]:
        ent["source_types"].append(src_type)
    ent["source_types"] = sorted(ent["source_types"])

    pg = r.get("__page_num")
    if pg and pg not in ent["pages"]:
        ent["pages"].append(pg)
    ent["pages"] = sorted(ent["pages"])

    try:
        conf = float(r.get("confidence", 0.0) or 0.0)
    except Exception:
        conf = 0.0
    ent["max_confidence"] = max(ent["max_confidence"], conf)

    note = normalize_whitespace(r.get("notes", ""))
    if note and note not in ent["notes_list"]:
        ent["notes_list"].append(note)

    if rec_name and rec_name != ent["teacher_name"] and rec_name not in ent["aliases"]:
        ent["aliases"].append(rec_name)

def score_timetable_to_roster_match(t_rec: dict, ent: dict) -> float:
    sim   = name_similarity(t_rec.get("teacher_name", ""), ent.get("teacher_name", ""))
    score = sim
    if subjects_overlap(t_rec.get("subjects", []) or [], ent.get("subjects", []) or []):
        score += 0.35
    t_main = choose_main_subject(t_rec.get("subjects", []) or [])
    e_main = choose_main_subject(ent.get("subjects", []) or [])
    if t_main and e_main and t_main == e_main:
        score += 0.25
    if is_uncertain_record(t_rec):
        score -= 0.05
    return score

def is_high_confidence_timetable_new_person(t_rec: dict) -> bool:
    try:
        conf = float(t_rec.get("confidence", 0.0) or 0.0)
    except Exception:
        conf = 0.0
    notes     = normalize_whitespace(t_rec.get("notes", ""))
    uncertain = bool(re.search(r"(不鮮明|不明|判読|暫定|推定|可能性)", notes))
    return conf >= 0.90 and not uncertain

def finalize_entities(entities):
    teachers = []
    for ent in entities:
        source_type_set = set(ent["source_types"])
        if "roster" in source_type_set and "timetable" in source_type_set:
            presence = "both"
        elif "timetable" in source_type_set:
            presence = "timetable_only"
        elif "roster" in source_type_set:
            presence = "roster_only"
        else:
            presence = "other"

        notes = " ".join(ent["notes_list"]).strip()
        if ent["aliases"]:
            alias_txt = "Merged aliases: " + ", ".join(sorted(set(ent["aliases"])))
            notes = normalize_whitespace((notes + " " + alias_txt).strip())

        teachers.append({
            "teacher_name":      ent["teacher_name"],
            "subjects":          sort_subjects(ent["subjects"]),
            "classes_or_grades": sorted(ent["classes_or_grades"]),
            "source_files":      sorted(ent["source_files"]),
            "source_types":      sorted(ent["source_types"]),
            "presence":          presence,
            "notes":             notes,
            "pages":             sorted(ent["pages"]),
            "max_confidence":    ent["max_confidence"],
        })
    return sorted(teachers, key=lambda x: normalize_teacher_key(x["teacher_name"]))

def merge_master(all_records):
    roster_records    = [r for r in all_records if r.get("source_type") == "roster"]
    timetable_records = [r for r in all_records if r.get("source_type") == "timetable"]
    other_records     = [r for r in all_records if r.get("source_type") not in {"roster", "timetable"}]

    # ── build base entities from roster ──
    roster_entities = []
    for r in roster_records:
        r_name = clean_teacher_name(r.get("teacher_name", ""))
        r_key  = normalize_teacher_key(r_name)
        r_subs = r.get("subjects", []) or []
        matched_idx, best_score = None, -1.0

        for idx, ent in enumerate(roster_entities):
            sim  = name_similarity(r_name, ent["teacher_name"])
            same = subjects_overlap(r_subs, ent["subjects"])
            score = sim + (0.30 if same else 0.0)
            if r_key and r_key == ent["teacher_name_key"]:
                matched_idx, best_score = idx, 999
                break
            if score > best_score:
                best_score, matched_idx = score, idx

        if matched_idx is not None and (
            best_score == 999
            or best_score >= 1.10
            or (best_score >= 0.92 and name_similarity(r_name, roster_entities[matched_idx]["teacher_name"]) >= 0.85)
        ):
            merge_record_into_entity(roster_entities[matched_idx], r, keep_roster_name=False)
        else:
            roster_entities.append(build_entity_from_record(r))

    # ── merge timetable into roster ──
    unmatched_timetable_entities = []
    for t in timetable_records:
        best_idx, best_score, best_sim = None, -1.0, 0.0
        for idx, ent in enumerate(roster_entities):
            score = score_timetable_to_roster_match(t, ent)
            sim   = name_similarity(t.get("teacher_name", ""), ent.get("teacher_name", ""))
            if score > best_score:
                best_score, best_sim, best_idx = score, sim, idx

        matched = False
        if best_idx is not None:
            t_main = choose_main_subject(t.get("subjects", []) or [])
            e_main = choose_main_subject(roster_entities[best_idx].get("subjects", []) or [])
            same_main = t_main and e_main and t_main == e_main
            if best_score >= 1.10 or (same_main and best_sim >= 0.78) or best_sim >= 0.90:
                matched = True

        if matched:
            merge_record_into_entity(roster_entities[best_idx], t, keep_roster_name=True)
        elif is_high_confidence_timetable_new_person(t):
            unmatched_timetable_entities.append(build_entity_from_record(t))

    # ── merge other records ──
    for r in other_records:
        r_name = clean_teacher_name(r.get("teacher_name", ""))
        r_key  = normalize_teacher_key(r_name)
        matched_idx, best_score = None, -1.0
        for idx, ent in enumerate(roster_entities):
            sim  = name_similarity(r_name, ent["teacher_name"])
            same = subjects_overlap(r.get("subjects", []) or [], ent["subjects"])
            score = sim + (0.25 if same else 0.0)
            if r_key and r_key == ent["teacher_name_key"]:
                matched_idx, best_score = idx, 999
                break
            if score > best_score:
                best_score, matched_idx = score, idx

        if matched_idx is not None and (best_score == 999 or best_score >= 0.95):
            merge_record_into_entity(roster_entities[matched_idx], r, keep_roster_name=True)
        else:
            unmatched_timetable_entities.append(build_entity_from_record(r))

    entities = roster_entities + unmatched_timetable_entities

    # ── final light dedupe ──
    final_entities = []
    for ent in entities:
        matched_idx, best_score = None, -1.0
        for idx, f in enumerate(final_entities):
            sim  = name_similarity(ent["teacher_name"], f["teacher_name"])
            same = subjects_overlap(ent["subjects"], f["subjects"])
            score = sim + (0.25 if same else 0.0)
            if ent["teacher_name_key"] and ent["teacher_name_key"] == f["teacher_name_key"]:
                matched_idx, best_score = idx, 999
                break
            if score > best_score:
                best_score, matched_idx = score, idx

        if matched_idx is not None and (best_score == 999 or best_score >= 1.12):
            keep_roster = "roster" in final_entities[matched_idx]["source_types"]
            merge_record_into_entity(final_entities[matched_idx], {
                "teacher_name":      ent["teacher_name"],
                "subjects":          ent["subjects"],
                "classes_or_grades": ent["classes_or_grades"],
                "__file":            ent["source_files"][0] if ent["source_files"] else "",
                "source_type":       ent["source_types"][0] if ent["source_types"] else "other",
                "__page_num":        ent["pages"][0] if ent["pages"] else None,
                "confidence":        ent["max_confidence"],
                "notes":             " ".join(ent["notes_list"]).strip(),
            }, keep_roster_name=keep_roster)
            fe = final_entities[matched_idx]
            for attr, src in [
                ("source_files", ent["source_files"]),
                ("source_types", ent["source_types"]),
                ("pages",        ent["pages"]),
                ("notes_list",   ent["notes_list"]),
                ("aliases",      ent["aliases"]),
            ]:
                for item in src:
                    if item and item not in fe[attr]:
                        fe[attr].append(item)
            fe["max_confidence"] = max(fe["max_confidence"], ent["max_confidence"])
            fe["subjects"]          = sort_subjects(fe["subjects"])
            fe["classes_or_grades"] = sorted(set(fe["classes_or_grades"]))
            fe["source_files"]      = sorted(set(fe["source_files"]))
            fe["source_types"]      = sorted(set(fe["source_types"]))
            fe["pages"]             = sorted(set(fe["pages"]))
        else:
            final_entities.append(ent)

    return finalize_entities(final_entities)

