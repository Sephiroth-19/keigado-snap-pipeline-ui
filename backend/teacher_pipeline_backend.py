def _call_gpt_card_once(image_path: str, prompt: str) -> dict:
    """Single GPT vision call for a card image. Returns parsed dict or error dict."""
    ext  = Path(image_path).suffix.lower().lstrip(".")
    mime = "image/jpeg" if ext in ("jpg", "jpeg") else f"image/{ext}"
    b64  = encode_image_file(image_path)
    response = client.chat.completions.create(
        model=MODEL_VISION,
        max_completion_tokens=500,
        messages=[{
            "role": "user",
            "content": [
                {"type": "text",      "text": prompt},
                {"type": "image_url", "image_url": {"url": f"data:{mime};base64,{b64}"}},
            ],
        }],
    )
    try:
        result = parse_json(response.choices[0].message.content)
        result.setdefault("confidence", None)
        result.setdefault("uncertain_chars", "")
        if result.get("uncertain_chars") is None:
            result["uncertain_chars"] = ""
        return result
    except Exception as e:
        return {"error": str(e), "raw": response.choices[0].message.content}


def extract_card_info(image_path: str) -> dict:
    """
    v8 — wraps the GPT vision call in a retry loop.

    Flow:
      1. First read with the standard CARD_EXTRACTION_PROMPT.
      2. If confidence < LOW_CONF_THRESHOLD (0.70):
           • Build a retry prompt = standard prompt + _RETRY_ADDENDUM_TEMPLATE
             (includes the first read's name/subject/uncertain chars so the
              model can explicitly reconsider those characters).
           • Make a second GPT call.
           • Keep whichever result has higher confidence.
      3. Return the best result.

    This helps cases like 13690076.JPG where the first read is completely
    wrong (小椋 命 instead of 村山 康介) — the retry often corrects it because
    the model is explicitly told to re-examine each flagged character.
    """
    print(f"  [image] {Path(image_path).name}")

    # ── First read ────────────────────────────────────────────────────────────
    result1 = _call_gpt_card_once(image_path, CARD_EXTRACTION_PROMPT)

    if "error" in result1:
        return result1                  # unrecoverable parse error — return as-is

    conf1 = float(result1.get("confidence") or 0.0)

    if conf1 >= LOW_CONF_THRESHOLD:
        return result1                  # good enough — no retry needed

    # ── Low confidence: build retry prompt and call again ─────────────────────
    print(f"    ↳ low confidence ({conf1:.2f}), retrying with augmented prompt…")

    addendum = _RETRY_ADDENDUM_TEMPLATE.format(
        prev_name    = result1.get("name", ""),
        prev_subject = result1.get("subject", ""),
        uncertain    = result1.get("uncertain_chars", "") or "none flagged",
    )
    retry_prompt = CARD_EXTRACTION_PROMPT + "\n\n" + addendum

    result2 = _call_gpt_card_once(image_path, retry_prompt)

    if "error" in result2:
        # Retry failed to parse — keep the first result
        print(f"    ↳ retry parse error, keeping first read")
        return result1

    conf2 = float(result2.get("confidence") or 0.0)
    print(f"    ↳ retry confidence: {conf2:.2f}  (first was {conf1:.2f})")

    # Return whichever read has higher confidence
    # Tie-break: prefer the retry (it had more information)
    if conf2 >= conf1:
        result2["_retry_used"]       = True
        result2["_first_read_name"]  = result1.get("name", "")
        result2["_first_read_conf"]  = conf1
        return result2
    else:
        result1["_retry_used"]       = True
        result1["_retry_read_name"]  = result2.get("name", "")
        result1["_retry_read_conf"]  = conf2
        return result1

